#QUERY FOR CHECKING COMPLETION
SELECT A.SCRAPPER, A.TIPO, COUNT(*) FROM
(
SELECT 
CASE 
	WHEN LYRICS='NA' THEN 'NOT AVAILABLE' 
	WHEN LYRICS IS NULL THEN 'PENDING'
	ELSE 'FOUND' end  as Tipo,
    CASE 
    WHEN id >= 1 AND id  <= 128895 THEN 'IVAN' 
    WHEN id >= 128896 AND id  <= 257790 THEN 'KATE' 
    WHEN id >= 257791 AND id  <= 386685 THEN 'SEBASTIAN' 
    WHEN id >= 386686 AND id  <= 515580 THEN 'MARCELO' 
    ELSE 'NONE' END AS SCRAPPER
FROM songs_dataset) A
GROUP BY A.SCRAPPER,A.TIPO
ORDER BY 1, 2;

